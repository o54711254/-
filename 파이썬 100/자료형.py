# 자료형이 무엇인지 확인할 때 사용하는 함수는 무엇인가?
# 자료형을 보여주는 함수를 사용하여 자료형 출력 예제를 만들어보시오.

# [!] : 자료형이란 무엇인가?
# 자료형 또는 데이터타입이라고 부른다.
# 프로그램 코드를 작성하는 과정에서 여러 종류의 데이터 값을 사용하여 정보처리를 하게 된다.
# 이때, 여러 종류의 데이터를 구분할 필요가 있는데 자료형을 통해서 이러한 데이터 종류를 분류한다.
# 정수, 실수, 문자열, 리스트, 튜플, 집합, 딕셔너리 등등
# type() 함수를 사용하여 자료형을 확인

# [1]
a = 100
print(a, type(a))

# [2]
b = 3.14
print(b, type(b))

# [3]
c = 'korea'
print(c, type(c))

# [4]
d = '010-5471-1254'
print(d, type(d))

# [5] : 리스트형
lst = [1, 2, 3, 4, 5]
print(lst, type(lst))

# [6] : 튜플형
tpl = (1, 2, 3, 4, 5)
print(tpl, type(tpl))

# [7] : 집합형(중복 X)
s = {1, 2, 3, 4, 5}
print(s, type(s))

# [8] : 딕셔너리(사전형, 왼쪽 오른쪽이 쌍을 이뤄서 들어감)
d = {'a':97, 'b':98}
print(d,type(d))


print()


