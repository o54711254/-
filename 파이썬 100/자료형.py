# 아스키 코드란 무엇인가

# [1] : 아스키 코드란?
# 컴퓨터는 내부에서 숫자 또는 문자에 대한 정보처리를 이진수로 처리.
# 이때, 문자와 숫자를 대응시켜줘야 하는데 여러 방법중 아스키 코드 방식을 사용한다.

# [2] : 몇개는 외우는게 좋다.
# 대문자 A : 65 (이후부터는 하나씩 증가 --> B : 66)
# 소문자 a : 97 --> b : 98
# 숫자 0 : 48
# 엔터(Enter) : 13
# 아스키코드 0번 : NULL


# 대문자 A, B, C에 대해서 아스키 코드로 출력하는 코드를 작성하시오(ord 함수 사용)

# [1] : 대문자 A
print(ord('A'))

# [2] : 소문자 a
print(ord('a'))


print()


# 사용자로부터 숫자를 입력받아 그에 해당하는 문자를 출력하는 프로그램을 구현하시오.

# [1] : 사용자 입력
a = input('숫자를 입력하면 해당하는 문자를 출력해드려요 =')
print(a, type(a))           # str로 출력

# [2] : 정수형으로 자료형을 변환
a = int(a)
print(a, type(a))

# [3] : 해당하는 값을 출력하기(chr 함수 사용)
chr_ = chr(a)
print('당신이 입력한 숫자의 문자는:', chr_)
print(f'당신이 입력한 문자는 {chr_} 입니다.')